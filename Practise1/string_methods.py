# --- Основные методы работы со строками в Python ---

# 1. Изменение регистра
# text.upper()      # В верхний регистр
# text.lower()      # В нижний регистр
# text.capitalize() # Первая буква заглавная
# text.title()      # Каждое слово с заглавной буквы
# text.swapcase()   # Инверсия регистра

# 2. Очистка и удаление
# text.strip()      # Убрать пробелы по краям
# text.lstrip()     # Убрать пробелы слева
# text.rstrip()     # Убрать пробелы справа

# 3. Поиск и замена
# text.find(sub)    # Индекс вхождения (-1 если не найдено)
# text.index(sub)   # Индекс вхождения (ошибка если не найдено)
# text.replace(a, b)# Замена подстроки 'a' на 'b'
# text.count(sub)   # Количество вхождений

# 4. Проверка (возвращают True/False)
# text.isdigit()    # Состоит из цифр?
# text.isalpha()    # Состоит из букв?
# text.isalnum()    # Буквы или цифры?
# text.startswith(s)# Начинается с 's'?
# text.endswith(s)  # Заканчивается на 's'?

# 5. Разделение и сборка
# text.split(sep)   # Разбить строку в список по разделителю
# text.join(list)   # Собрать список в строку с разделителем
# text.splitlines() # Разбить по символам переноса строки

# 6. Выравнивание
# text.center(w)    # Центрировать в поле шириной w
# text.ljust(w)     # Выровнять по левому краю
# text.rjust(w)     # Выровнять по правому краю
# text.zfill(w)     # Заполнить нулями слева до ширины w